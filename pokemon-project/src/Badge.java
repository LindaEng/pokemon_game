
public class Badge {
	private String name;
	private String gym;
	
	public Badge() {
		
	}
	
	public String getGym() {
		return gym;
	}
	
	public String getName() {
		return name;
	}
	
	public void setGym(String gym) {
		this.gym = gym;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	
}
